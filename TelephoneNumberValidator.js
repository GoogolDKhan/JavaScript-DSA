function telephoneCheck(str) {
  const regexs = [
    /^\d{3}-\d{3}-\d{4}$/, // 555-555-5555
    /^1 \d{3}-\d{3}-\d{4}$/, // 1 555-555-5555
    /^1 \(\d{3}\) \d{3}-\d{4}$/, // 1 (555) 555-5555
    /^\d{10}$/, // 5555555555
    /^\(\d{3}\)\d{3}-\d{4}$/, // (555)555-5555
    /^1 \d{3} \d{3} \d{4}$/, // 1 555 555 5555
    /1\(\d{3}\)\d{3}-\d{4}/, // 1(555)555-5555
  ];
  return regexs.some((pattern) => pattern.test(str));
}

telephoneCheck("555-555-5555");
